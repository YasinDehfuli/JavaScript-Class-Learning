class School {
  constructor(name,
              address,
              street,
              city,
              state,
              postalCode,
              country,
              yearFounded,
              area,
              type,
              email,
              website,
              budget,
              motto,
              investor,
              numberOfTeachers,
              numberOfStudents) {
    this.name = name;
    this.address = address;
    this.street = street;
    this.city = city;
    this.state = state;
    this.postalCode = postalCode;
    this.country = country;
    this.yearFounded = yearFounded;
    this.area = area;
    this.type = type;
    this.email = email;
    this.website = website;
    this.budget = budget;
    this.motto = motto;
    this.investor = investor;
    this.numberOfTeachers = numberOfTeachers;
    this.numberOfStudents = numberOfStudents;
  }
}